#include <string.h>

char	*ft_strncpy(char *s1, const char *s2, size_t len)
{
	size_t	i;

	if (len == 0)
		return (s1);
	i = 0;
	while (i < len && s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	while (i < len)
	{
		s1[i] = '\0';
		i++;
	}
	return (s1);
}
